# geef 2 getallen op en kijk of ze gelijk zijn
# 5 en 5 zijn gelijk
# 5 en 3 zijn niet gelijk

get1 = int(input('geef getal'))
get2 = int(input('geef nog getal'))

if get1 == get2:
    print(f'{get1} en {get2} zijn gelijk')
if get1 != get2:
    print(f'{get1} en {get2} zijn niet gelijk')

# geef 2 getallen op en kijk of getal1 groter is dan getal2
# 5 is groter dan 3
# 3 is niet groter dan 5

if get1 > get2:
    print(f'{get1} is groter dan {get2}')
if get1 <= get2:
    print(f'{get1} is niet groter dan {get2}')

